const API_URL = 'http://localhost:3000/api/buybox-batch';
const BATCH_SIZE = 20; // SP-API limit
const DELAY_MS = 2500; // 2.5 seconds (Stay safe under 0.5 req/sec limit)

async function runCheck() {
  console.log(`🚀 Starting Buy Box check for ${SKUS.length} SKUs...`);
  
  let allResults = [];

  // Helper to chunk array
  const chunkArray = (arr, size) => {
    const chunks = [];
    for (let i = 0; i < arr.length; i += size) {
      chunks.push(arr.slice(i, i + size));
    }
    return chunks;
  };

  const SKU_CHUNKS = chunkArray(SKUS, BATCH_SIZE);
  console.log(`📦 Split into ${SKU_CHUNKS.length} batches of ${BATCH_SIZE}`);

  const timestamp = new Date().toISOString().replace(/[:.]/g, '-');
  const csvFilename = `buybox_results_${timestamp}.csv`;
  const jsonFilename = `buybox_results_${timestamp}.json`;

  // Write CSV headers immediately
  const csvHeaders = ['SKU', 'ASIN', 'Status', 'Buy Box Price', 'Lowest Price', 'Next Best Price', 'Currency', 'Our Price', 'Winning', 'Offers'];
  fs.writeFileSync(csvFilename, csvHeaders.join(',') + '\n');

  for (let i = 0; i < SKU_CHUNKS.length; i++) {
    const chunk = SKU_CHUNKS[i];
    process.stdout.write(`🔄 Processing batch ${i + 1}/${SKU_CHUNKS.length} (${( (i/SKU_CHUNKS.length)*100 ).toFixed(1)}%)... `);

    try {
      const response = await axios.post(API_URL, { skus: chunk });

      if (response.data.success) {
        const batchResults = response.data.results;
        allResults = [...allResults, ...batchResults];
        
        // Append to CSV as we go
        const csvRows = batchResults.map(item => {
          const d = item.data;
          return [
            `"${item.sku}"`,
            d?.asin || '',
            item.status === 'success' ? 'Success' : 'Error',
            d?.buyBoxPrice || '',
            d?.lowestPrice || '',
            d?.nextBestPrice || '',
            d?.currency || '',
            d?.ourPrice || '',
            d?.isWinner ? 'Yes' : 'No',
            d?.offerCount || 0
          ].join(',');
        });
        
        fs.appendFileSync(csvFilename, csvRows.join('\n') + '\n');
        process.stdout.write(`✅ Success\n`);
      } else {
        process.stdout.write(`❌ Batch failed: ${response.data.error}\n`);
      }
    } catch (error) {
       process.stdout.write(`❌ Error: ${error.response?.data?.error || error.message}\n`);
    }

    // Rate limiting delay
    if (i < SKU_CHUNKS.length - 1) {
      await new Promise(resolve => setTimeout(resolve, DELAY_MS));
    }
  }

  // Final summary
  console.log('\n--- BUY BOX CHECK RESULTS SUMMARY ---');
  console.log(`Total SKUs checked: ${allResults.length}`);
  console.log(`Winning:            ${allResults.filter(r => r.data?.isWinner).length}`);
  console.log(`Not Winning:        ${allResults.filter(r => r.data && !r.data.isWinner).length}`);
  console.log(`Errors:             ${allResults.filter(r => r.status === 'error').length}`);

  // Save full JSON at the end
  fs.writeFileSync(jsonFilename, JSON.stringify({ results: allResults }, null, 2));
  
  console.log(`\n📊 Results stream-saved to: ${csvFilename}`);
  console.log(`💾 Full JSON saved to: ${jsonFilename}`);
}

runCheck().catch(err => {
  console.error('\nFatal Error:', err.message);
  if (err.code === 'ECONNREFUSED') {
    console.log('💡 Tip: SvelteKit server must be running on localhost:3000');
  }
});
